namespace ax::utility
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
/// \tparam T
///
///////////////////////////////////////////////////////////////////////////////
template <typename T>
struct remove_reference
{
    using type = T;
};

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
/// \tparam T
///
///////////////////////////////////////////////////////////////////////////////
template <typename T>
struct remove_reference<T&>
{
    using type = T;
};

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
/// \tparam T
///
///////////////////////////////////////////////////////////////////////////////
template <typename T>
struct remove_reference<T&&>
{
    using type = T;
};

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
/// \tparam T
///
/// \param arg
///
/// \return
///
///////////////////////////////////////////////////////////////////////////////
template <typename T>
typename remove_reference<T>::type&& move(T&& arg) noexcept
{
    return (static_cast<typename remove_reference<T>::type&&>(arg));
}

} // namespace ax::utility

